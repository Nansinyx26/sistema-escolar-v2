document.addEventListener('DOMContentLoaded', async () => {
    // 1. Tenta carregar dados atualizados do MongoDB (fallback automático para hardcoded)
    if (window.HorarioSync) {
        await HorarioSync.load();
    }

    // 2. Renderiza a visão solicitada via URL params
    const p = new URLSearchParams(window.location.search);
    const turmaParam = p.get('turma');
    const profParam = p.get('prof');
    const tabParam = p.get('tab');

    if (turmaParam) {
        const s = document.getElementById('sel-turma'); if (s) s.value = turmaParam;
        mostrarTabela(turmaParam, 'turma');
    } else if (profParam) {
        const s = document.getElementById('sel-prof'); if (s) s.value = profParam;
        mostrarTabela(profParam, 'prof');
    } else if (tabParam === 'geral') {
        mostrarTabela('GERAL', 'geral');
    }
});

function showToast(message, type = 'success') {
    let toast = document.querySelector('.toast-notification');
    if (!toast) {
        toast = document.createElement('div');
        toast.className = 'toast-notification';
        document.body.appendChild(toast);
    }
    
    const icon = type === 'success' ? 'bi-check-circle' : 'bi-exclamation-triangle';
    toast.innerHTML = `<i class="bi ${icon}"></i> ${message}`;
    toast.className = `toast-notification ${type} show`;
    
    setTimeout(() => {
        toast.classList.remove('show');
    }, 3000);
}

function toggleLegendaAccordion(btn) {
    const content = btn.nextElementSibling;
    const isExpanded = btn.getAttribute('aria-expanded') === 'true';
    
    // Close all other accordions
    document.querySelectorAll('.legenda-accordion-content').forEach(c => {
        if (c !== content) {
            c.hidden = true;
            c.previousElementSibling.setAttribute('aria-expanded', 'false');
            c.previousElementSibling.querySelector('span').innerHTML = c.previousElementSibling.querySelector('span').innerHTML.replace('▲', '▼');
        }
    });

    btn.setAttribute('aria-expanded', !isExpanded);
    content.hidden = isExpanded;
    
    const span = btn.querySelector('span');
    if (isExpanded) {
        span.innerHTML = span.innerHTML.replace('▲', '▼');
    } else {
        span.innerHTML = span.innerHTML.replace('▼', '▲');
    }
}
