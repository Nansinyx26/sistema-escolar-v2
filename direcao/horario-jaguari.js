function toggleMobileTableView() {
    const tableWrapper = document.querySelector('.table-full-mode');
    const cardsContainer = document.getElementById('horario-cards-mobile');
    const btn = document.getElementById('btn-toggle-table');
    if (!tableWrapper || !btn) return;

    const isHidden = !tableWrapper.classList.contains('show-mobile');
    if (isHidden) {
        tableWrapper.classList.add('show-mobile');
        if (cardsContainer) cardsContainer.style.display = 'none';
        btn.innerHTML = '<i class="bi bi-grid-3x3-gap"></i> Ver Cards por Turma';
    } else {
        tableWrapper.classList.remove('show-mobile');
        if (cardsContainer) cardsContainer.style.display = '';
        btn.innerHTML = '<i class="bi bi-table"></i> Ver Tabela Completa';
    }
}

function renderMobileCards() {
    const container = document.getElementById('horario-cards-mobile');
    if (!container) return;

    const turmasIds = ['1A', '1B', '1C', '2A', '2B', '2C', '3A', '3B', '3C', '4A', '4B', '4C', '5A', '5B', '5C', '5D'];
    const aulasIndices = [0, 1, 3, 4, 5, 7, 8];

    let html = '<div class="horario-cards-grid">';
    turmasIds.forEach(id => {
        const profName = SALAS_PROF[id] || '';
        const disciplinasPorDia = DIAS_CURTOS.map((diaLabel, diaIdx) => {
            let aulas = [];
            if (window.turmasData && window.turmasData[id]) {
                const turmaHorarios = window.turmasData[id].horarios || [];
                aulas = aulasIndices.map(idx => {
                    const row = turmaHorarios[idx];
                    return (row && row.aulas) ? row.aulas[diaIdx] : '';
                }).filter(a => a && a !== 'PEB 1' && a !== 'PEB1' && a !== '—');
            }
            const resumo = aulas.length ? aulas.join(', ') : '—';
            return `<div class="horario-card-dia"><strong>${diaLabel}:</strong> ${resumo}</div>`;
        }).join('');

        const turmaDisplay = id.replace(/(\d)([A-D])/, '$1º $2');
        html += `
            <div class="horario-card-turma" onclick="mostrarTabela('${id}', 'turma')">
                <div class="horario-card-header">
                    ${turmaDisplay}
                    <div style="font-size:0.75rem;opacity:0.85;font-weight:400;margin-top:2px">Profª ${profName}</div>
                </div>
                <div class="horario-card-body">${disciplinasPorDia}</div>
            </div>`;
    });
    html += '</div>';
    container.innerHTML = html;
}

